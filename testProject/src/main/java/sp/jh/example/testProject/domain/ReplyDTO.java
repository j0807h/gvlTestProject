public class ReplyDTO {
	private Integer boardNum;
	private Integer replyNum;
	private String replyWriter;
	private String replyDelFlag;
	private String replyMemo;
	private String replyDate;
	private String replyParent;
	private String replyDepth;
	private String replyOrder;
	
	public Integer getBoardNum() {
		return boardNum;
	}
	public void setBoardNum(Integer boardNum) {
		this.boardNum = boardNum;
	}
	public Integer getReplyNum() {
		return replyNum;
	}
	public void setReplyNum(Integer replyNum) {
		this.replyNum = replyNum;
	}
	public String getReplyWriter() {
		return replyWriter;
	}
	public void setReplyWriter(String replyWriter) {
		this.replyWriter = replyWriter;
	}
	public String getReplyDelFlag() {
		return replyDelFlag;
	}
	public void setReplyDelFlag(String replyDelFlag) {
		this.replyDelFlag = replyDelFlag;
	}
	public String getReplyMemo() {
		return replyMemo;
	}
	public void setReplyMemo(String replyMemo) {
		this.replyMemo = replyMemo;
	}
	public String getReplyDate() {
		return replyDate;
	}
	public void setReplyDate(String replyDate) {
		this.replyDate = replyDate;
	}
	public String getReplyParent() {
		return replyParent;
	}
	public void setReplyParent(String replyParent) {
		this.replyParent = replyParent;
	}
	public String getReplyDepth() {
		return replyDepth;
	}
	public void setReplyDepth(String replyDepth) {
		this.replyDepth = replyDepth;
	}
	public String getReplyOrder() {
		return replyOrder;
	}
	public void setReplyOrder(String replyOrder) {
		this.replyOrder = replyOrder;
	}
	
	
}
